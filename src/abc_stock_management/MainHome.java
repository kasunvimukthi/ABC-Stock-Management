    public boolean verifyFields12()
    {
        String name = jTextField13.getText();
        String id = jTextField46.getText();
        String address = jTextField47.getText();
        String email = jTextField48.getText();
        
        String phone = jTextField49.getText();
        
        // check empty fields
        if(name.trim().equals("") || phone.trim().equals("") || address.trim().equals("") ||  email.trim().equals("") || id.trim().equals("") )
        {
            JOptionPane.showMessageDialog(null, "One Or More Fields Are Empty","Empty Fields",2);
            return false;
        }
        if (!(Pattern.matches("^[a-zA-Z0-9]+[@]{1}+[a-zA-Z0-9]+[.]{1}+[a-zA-Z0-9]+$", jTextField48.getText()))) {
            JOptionPane.showMessageDialog(null, "Please enter a valid email", "Warning", JOptionPane.WARNING_MESSAGE);
            return false;

        }
        
        
        // if everything is ok
        else{
            return true;
        }
    }
